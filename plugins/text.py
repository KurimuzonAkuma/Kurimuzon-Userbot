async def convert_to_emojis(text: str, emoji_map: dict, lower: bool = False):
    text = text.lower() if lower else text
    result = ""
    for _ in text:
        for key, value in emoji_map.items():
            if text.startswith(key):
                result += value
                text = text.replace(key, "", 1)
                break
    return result


