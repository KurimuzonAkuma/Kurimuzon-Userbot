    args = get_args_raw(message)

    old_clients = wg.get_clients()
    if not old_clients:
        await message.edit_text("<b>No users found</b>")
        return

    start_time = datetime.datetime.now()
    await asyncio.sleep(1)
    new_clients = wg.get_clients()
